// ── Main ──────────────────────────────────────────────────────────────────────

async function main() {
  loadEnv();

  const url = process.env.NEXT_PUBLIC_SUPABASE_URL;
  const key = process.env.SUPABASE_SERVICE_ROLE_KEY;
  if (!url || !key) {
    console.error("✗ NEXT_PUBLIC_SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY not set in .env.local");
    process.exit(1);
  }

  const db = createClient(url, key, { auth: { persistSession: false } });

  // 1. Upsert new sub-brands
  console.log(`\nUpserting ${NEW_SUB_BRANDS.length} new sub-brands…`);
  const { error: se } = await db.from("sub_brands").upsert(NEW_SUB_BRANDS, { onConflict: "id" });
  if (se) { console.error("✗ sub_brands:", se.message); process.exit(1); }
  NEW_SUB_BRANDS.forEach((s) => console.log(`  ✓ ${s.id}  (${s.name})`));

  // 2. Upsert bottles in batches of 20
  console.log(`\nUpserting ${BOTTLES.length} bottles…`);
  const BATCH = 20;
  let count = 0;
  for (let i = 0; i < BOTTLES.length; i += BATCH) {
    const batch = BOTTLES.slice(i, i + BATCH);
    const { error: bte } = await db.from("bottles").upsert(batch, { onConflict: "id" });
    if (bte) { console.error(`✗ bottles batch ${i}:`, bte.message); process.exit(1); }
    batch.forEach((b) => { console.log(`  ✓ ${b.id}`); count++; });
  }

  console.log(`\n✅ Done — ${NEW_SUB_BRANDS.length} sub-brands, ${count} bottles upserted.`);
}

main().catch((e) => { console.error(e); process.exit(1); });
